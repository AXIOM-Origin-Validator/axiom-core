#[cfg(test)]
pub fn print_test_validator_ids() {
    println!("\n# Valid validator IDs with BLAKE3 checksums");
    println!("# Paste this into test-fixtures/test_config.json");
    println!("VALID_VALIDATOR_IDS = {{");
    for i in 1..=6 {
        let email = format!("v{}@axiom.validators", i);
        let salt = format!("{:02x}", i);
        let validator_id = generate_wallet_id(&email, &salt, &[0u8; 32]).unwrap();
        let carrier = format!("mailto:v{}@axiom.validators", i);
        println!("    \"V{}\": {{", i);
        println!("        \"id\": \"{}\",", validator_id);
        println!("        \"carrier\": \"{}\",", carrier);
        println!("    }},");
    }
    println!("}}");
}

#[test]
fn gen_test_wallet_ids() {
    print_test_wallet_ids();
}

#[test]
fn gen_test_validator_ids() {
    print_test_validator_ids();
}
