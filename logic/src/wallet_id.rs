/// S-ABR overlap: strict majority floor(k/2) + 1.
/// Prevents double-spend — attacker cannot present conflicting TX to fresh validators.
pub fn sabr_overlap(k: u8) -> u8 {
    if k == 0 { return 0; }
    (k / 2) + 1
}

