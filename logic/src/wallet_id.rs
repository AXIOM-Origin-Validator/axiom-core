/// Normalize an email address for consistent checksum computation.
/// 
/// SECURITY (C2 - Semantic Confusion Prevention):
/// - Converts to lowercase (ASCII only)
/// - Trims whitespace
/// - Rejects non-ASCII characters (prevents homoglyph attacks like "аlice" vs "alice")
/// 
/// This ensures the same email is always hashed the same way,
/// preventing field aliasing attacks where different representations
/// of the "same" email produce different checksums.
fn normalize_email(email: &str) -> CoreResult<String> {
    let trimmed = email.trim();
    
    // Reject empty email
    if trimmed.is_empty() {
        return Err(ValidationError::MalformedAddress);
    }
    
    // SECURITY: Reject non-ASCII characters
    // This prevents homoglyph attacks where Cyrillic 'а' looks like Latin 'a'
    // Example attack: "аlice@test.com" (Cyrillic а) vs "alice@test.com" (Latin a)
    if !trimmed.is_ascii() {
        return Err(ValidationError::MalformedAddress);
    }
    
    // Convert to lowercase for consistency
    Ok(trimmed.to_ascii_lowercase())
}

/// Strip encryption suffix (-P, -G, etc.) from a wallet_id.
/// "bob@example.com/a3f7b2ff32-P" → "bob@example.com/a3f7b2ff32"
pub fn strip_encryption_suffix(wallet_id: &str) -> String {
    // Suffixes: -P (PGP), -G (GPG). Always at the very end after hex10.
    if wallet_id.ends_with("-P") || wallet_id.ends_with("-G") {
        String::from(&wallet_id[..wallet_id.len() - 2])
    } else {
        String::from(wallet_id)
    }
}

/// Strip email change suffix (-01, -02, ..., -99) from wallet_id.
/// Returns (clean_wallet_id, change_count). change_count = None if no suffix.
///
/// Format: "email/hex10-XX" where XX is 01-99.
/// Only matches if the part before -XX ends with 10 hex chars after the last '/'.
/// Stripped AFTER encryption suffix: "email/hex10-01-P" → strip -P → strip -01.
pub fn strip_email_change_suffix(wallet_id: &str) -> (String, Option<u8>) {
    let s = wallet_id;
    if s.len() < 3 {
        return (String::from(s), None);
    }

    // The valid "-XX" suffix is exactly 3 ASCII bytes. If `len - 3` is not a
    // char boundary, the tail is a multi-byte UTF-8 char (e.g. a 4-byte emoji)
    // and cannot be a suffix — return safely instead of panicking on a mid-char
    // byte slice. DoS fix: a hostile wallet_id (e.g. a receiver_wallet_id inside
    // a TX) ending in a multi-byte char must NEVER unwind Core. The decoder
    // fails closed (no suffix), never panics.
    let cut = s.len() - 3;
    if !s.is_char_boundary(cut) {
        return (String::from(s), None);
    }
    let last3 = &s[cut..];
    if !last3.starts_with('-') {
        return (String::from(s), None);
    }

    let digits = &last3[1..];
    if digits.len() != 2 || !digits.chars().all(|c| c.is_ascii_digit()) {
        return (String::from(s), None);
    }

    let n: u8 = match digits.parse() {
        Ok(n) if n >= 1 => n,
        _ => return (String::from(s), None),
    };

    // Verify the part before -XX has a valid hex10 structure:
    // Must have exactly 10 hex chars after the last '/' (with optional dashes for display)
    // `cut` is the verified char boundary at len-3 (see above) — safe to slice.
    let base = &s[..cut];
    if let Some(slash_pos) = base.rfind('/') {
        let hex_part = &base[slash_pos + 1..];
        let hex_clean: String = hex_part.chars().filter(|c| *c != '-').collect();
        if hex_clean.len() == 10 && hex_clean.chars().all(|c| c.is_ascii_hexdigit()) {
            return (String::from(base), Some(n));
        }
    }

    (String::from(s), None)
}

/// Check if a wallet_id requires receiver_address (has -XX email change suffix).
pub fn requires_receiver_address(wallet_id: &str) -> bool {
    let clean = strip_encryption_suffix(wallet_id);
    let (_, change) = strip_email_change_suffix(&clean);
    change.is_some()
}

/// Parse a wallet_id into (email, checksum, pk_bind, salt)
///
/// wallet_id format: "email/hex10" or "email/hex10-P" (encryption suffix stripped)
/// hex10 = checksum(6) + pk_bind(2) + salt(2)
/// Example: "bob@example.com/a3f7b2ff32" → ("bob@example.com", "a3f7b2", "ff", "32")
///
/// checksum(6): anti-typo + security level extraction (BLAKE3, no pk)
/// pk_bind(2): identity binding (BLAKE3 with pk)
/// salt(2): random salt
///
/// SECURITY: Email is normalized before returning.
pub fn parse_wallet_id(wallet_id: &str) -> CoreResult<(String, String, String, String)> {
    // Strip suffixes: -P (encryption), then -XX (email change)
    let after_pgp = strip_encryption_suffix(wallet_id);
    let (clean, _) = strip_email_change_suffix(&after_pgp);

    // Find the last '/' separator
    let slash_pos = clean
        .rfind('/')
        .ok_or(ValidationError::MalformedAddress)?;

    let email_raw = &clean[..slash_pos];
    let hex_part = &clean[slash_pos + 1..];

    // Normalize email (lowercase, trim, ASCII-only)
    let email = normalize_email(email_raw)?;

    // Validate email contains @
    if !email.contains('@') {
        return Err(ValidationError::MalformedAddress);
    }

    // Clean hex part (remove dashes if any)
    let hex_clean: String = hex_part
        .chars()
        .filter(|c| *c != '-')
        .collect();

    // Must be exactly 10 hex chars (checksum6 + pk_bind2 + salt2)
    if hex_clean.len() != 10 {
        return Err(ValidationError::MalformedAddress);
    }

    // Validate all chars are hex
    if !hex_clean.chars().all(|c| c.is_ascii_hexdigit()) {
        return Err(ValidationError::MalformedAddress);
    }

    let checksum = hex_clean[0..6].to_lowercase();
    let pk_bind = hex_clean[6..8].to_lowercase();
    let salt = hex_clean[8..10].to_lowercase();

    Ok((email, checksum, pk_bind, salt))
}

/// Extract email from wallet_id
///
/// "bob@example.com/a3f7b2ff32" → "bob@example.com"
pub fn extract_email(wallet_id: &str) -> CoreResult<String> {
    let (email, _, _, _) = parse_wallet_id(wallet_id)?;
    Ok(email)
}

/// Compute the anti-typo checksum for a wallet_id (no pk — k extraction at CL1).
/// YPX-007: checksum = BLAKE3(email || MASTER_PK_hex || salt || k_byte || proof_type_byte)[0..3]
///
/// This checksum does NOT include pk — it's for anti-typo + security level extraction.
/// pk binding is done via a separate pk_bind byte (see compute_pk_bind).
fn compute_checksum(email: &str, salt: &str, master_pk: &[u8; 32], k_byte: u8, proof_type_byte: u8) -> String {
    let mut data = email.as_bytes().to_vec();
    data.extend_from_slice(hex::encode(master_pk).as_bytes());
    data.extend_from_slice(salt.as_bytes());
    data.push(k_byte);
    data.push(proof_type_byte);

    let hash = blake3_hash(&data);
    hex::encode(&hash[0..3])
}

/// Compute pk binding byte for a wallet_id.
/// pk_bind = BLAKE3("AXIOM_PK_BIND" || email || MASTER_PK || pk || salt || k || pt)[0..1] (2 hex chars)
///
/// This binds the wallet_id to the owner's Ed25519 public key.
/// Verified at CL5 to prevent cheque theft — only the pk that generated
/// the wallet_id can redeem cheques addressed to it.
fn compute_pk_bind(email: &str, salt: &str, master_pk: &[u8; 32], pk: &[u8; 32], k_byte: u8, proof_type_byte: u8) -> String {
    let mut data = b"AXIOM_PK_BIND".to_vec();
    data.extend_from_slice(email.as_bytes());
    data.extend_from_slice(master_pk);
    data.extend_from_slice(pk);
    data.extend_from_slice(salt.as_bytes());
    data.push(k_byte);
    data.push(proof_type_byte);

    let hash = blake3_hash(&data);
    hex::encode(&hash[0..1])
}

/// Compute checksum using wallet_secret + pk (no WALLET_IDENTITY_KEY needed).
/// YP §31.9: checksum = BLAKE3(email || wallet_secret || pk_hex || salt || k_byte || pt_byte)[0..3]
fn compute_checksum_with_secret(
    email: &str, salt: &str, wallet_secret: &[u8; 32], pk: &[u8; 32],
    k_byte: u8, proof_type_byte: u8,
) -> String {
    let mut data = email.as_bytes().to_vec();
    data.extend_from_slice(wallet_secret);
    data.extend_from_slice(hex::encode(pk).as_bytes());
    data.extend_from_slice(salt.as_bytes());
    data.push(k_byte);
    data.push(proof_type_byte);
    let hash = blake3_hash(&data);
    hex::encode(&hash[0..3])
}

/// Generate a wallet_id using wallet_secret (no WALLET_IDENTITY_KEY).
pub fn generate_wallet_id_with_secret(
    email: &str, salt: &str, wallet_secret: &[u8; 32], pk: &[u8; 32],
) -> CoreResult<String> {
    generate_wallet_id_with_secret_full(email, salt, wallet_secret, pk, K_DEFAULT, PROOF_TYPE_DMAP)
}

/// Generate a wallet_id with explicit security level using wallet_secret.
/// Format: "email/checksum(6) + pk_bind(2) + salt(2)" = hex10
/// checksum from wallet_secret path, pk_bind from WALLET_IDENTITY_KEY + pk.
pub fn generate_wallet_id_with_secret_full(
    email: &str, salt: &str, wallet_secret: &[u8; 32], pk: &[u8; 32],
    k_byte: u8, proof_type_byte: u8,
) -> CoreResult<String> {
    let normalized_email = normalize_email(email)?;
    // char-boundary-safe: `get` yields None on a short or mid-char index → "00".
    let salt_clean = salt.get(0..2).unwrap_or("00");
    let checksum = compute_checksum_with_secret(
        &normalized_email, salt_clean, wallet_secret, pk, k_byte, proof_type_byte,
    );
    let pk_bind = compute_pk_bind(
        &normalized_email, salt_clean, &WALLET_IDENTITY_KEY, pk, k_byte, proof_type_byte,
    );
    Ok(format!("{}/{}{}{}", normalized_email, checksum, pk_bind, salt_clean))
}

/// Generate all 7 wallet_ids using wallet_secret.
pub fn generate_all_wallet_ids_with_secret(
    email: &str, salt: &str, wallet_secret: &[u8; 32], pk: &[u8; 32],
) -> CoreResult<Vec<(String, u8, u8, &'static str)>> {
    let mut result = Vec::with_capacity(7);
    for &(k_byte, pt_byte, name) in &WALLET_ID_PARAMS {
        let wid = generate_wallet_id_with_secret_full(
            email, salt, wallet_secret, pk, k_byte, pt_byte,
        )?;
        result.push((wid, k_byte, pt_byte, name));
    }
    Ok(result)
}

/// Verify a wallet_id was generated with a specific wallet_secret and pk.
/// Returns (k, proof_type) if match. Used by CL5 for wallet ownership proof.
pub fn verify_wallet_id_with_secret(
    wallet_id: &str, wallet_secret: &[u8; 32], pk: &[u8; 32],
) -> CoreResult<(u8, u8)> {
    let (email, checksum, _pk_bind, salt) = parse_wallet_id(wallet_id)?;
    for &(k_byte, pt_byte, _name) in &WALLET_ID_PARAMS {
        let candidate = compute_checksum_with_secret(
            &email, &salt, wallet_secret, pk, k_byte, pt_byte,
        );
        if checksum == candidate.to_lowercase() {
            return Ok((k_byte, pt_byte));
        }
    }
    Err(ValidationError::InvalidWalletId)
}

/// Validate a wallet_id checksum (anti-typo + k extraction).
///
/// Verifies the checksum matches for some (k, pt) combination.
/// Does NOT verify pk_bind — use verify_pk_binding for that.
///
/// Encryption suffixes (-P, -G) are stripped before validation.
pub fn validate_wallet_id(wallet_id: &str) -> CoreResult<()> {
    extract_security_level(wallet_id).map(|_| ())
}

/// Extract (k, proof_type) from a wallet_id by trying all 7 combinations.
///
/// YPX-007: Core extracts the security level from the address — the sender
/// does not choose it. 7 BLAKE3 hashes is trivial (~1μs total).
///
/// Uses the checksum (first 6 hex) which does NOT include pk.
/// pk binding is verified separately via verify_pk_binding().
///
/// Returns (k, proof_type) on success, Err(InvalidWalletId) if no match.
pub fn extract_security_level(wallet_id: &str) -> CoreResult<(u8, u8)> {
    // Strip suffixes: -P (encryption), then -XX (email change) before checksum validation
    let after_pgp = strip_encryption_suffix(wallet_id);
    let (clean_id, _) = strip_email_change_suffix(&after_pgp);
    extract_security_level_with_identity_key(&clean_id, &WALLET_IDENTITY_KEY)
}

/// Extract (k, proof_type) with a specific identity key (for testing)
pub fn extract_security_level_with_identity_key(
    wallet_id: &str,
    master_pk: &[u8; 32],
) -> CoreResult<(u8, u8)> {
    let (email, checksum, _pk_bind, salt) = parse_wallet_id(wallet_id)?;

    for &(k_byte, pt_byte, _name) in &WALLET_ID_PARAMS {
        let candidate = compute_checksum(&email, &salt, master_pk, k_byte, pt_byte);
        if checksum == candidate.to_lowercase() {
            return Ok((k_byte, pt_byte));
        }
    }

    Err(ValidationError::InvalidWalletId)
}

/// FACT class isolation: true iff this wallet_id belongs to the dev
/// class (`AXIOM_DESIGN_FactClassIsolation.md` §7).
///
/// The dev class is signalled by an exact `@axiom.internal` email
/// domain. ICANN reserves `.internal` for private/internal-network
/// use, so it cannot clash with any public domain.
///
/// Match is strict and case-insensitive on the domain only:
/// - `developer@axiom.internal` → true
/// - `alice@axiom.com` → false
/// - `bob@axiom.internal.foo` → false
/// - `charlie@axiom` → false
pub fn is_dev_wallet(wallet_id: &str) -> bool {
    let after_pgp = strip_encryption_suffix(wallet_id);
    let (clean, _) = strip_email_change_suffix(&after_pgp);
    let email = clean.split('/').next().unwrap_or("");
    let mut parts = email.splitn(2, '@');
    let _local = parts.next();
    let domain = parts.next().unwrap_or("").to_ascii_lowercase();
    domain == "axiom.internal"
}

/// FACT class isolation rule R1
/// (`AXIOM_DESIGN_FactClassIsolation.md` §2.1, §3).
///
/// A transaction is valid only if sender and recipient share the same
/// class. Either both are dev (`@axiom.internal`) or neither is.
/// Symmetric: blocks `dev -> public` and `public -> dev` equally —
/// no one-way valve (silent-burn argument, design §3).
///
/// Protocol addresses (BURN / DEED / FEE / DWP) are exempt — they're
/// not real wallets and have no class. A dev wallet must remain able
/// to burn dev-AXC; a public wallet must remain able to burn AXC.
pub fn check_domain_isolation(
    sender_wallet_id: &str,
    recipient_wallet_id: &str,
) -> CoreResult<()> {
    let is_protocol = |w: &str| {
        w == crate::types::BURN_ADDRESS
            || w == crate::types::DEED_ADDRESS
            || w == crate::types::FEE_ADDRESS
            || w.starts_with(crate::types::DWP_ADDRESS_PREFIX)
    };
    if is_protocol(sender_wallet_id) || is_protocol(recipient_wallet_id) {
        return Ok(());
    }
    if is_dev_wallet(sender_wallet_id) != is_dev_wallet(recipient_wallet_id) {
        return Err(ValidationError::DomainMismatch);
    }
    Ok(())
}

/// Verify that a wallet_id's pk_bind byte matches the given public key.
///
/// This is the CL5 identity binding check. The pk_bind (2 hex chars at positions 6-7)
/// is a BLAKE3 hash of email + MASTER_PK + pk + salt + k + pt. Only the owner's pk
/// produces the correct pk_bind.
///
/// Call extract_security_level first to get (k, pt), then verify_pk_binding with pk.
pub fn verify_pk_binding(wallet_id: &str, pk: &[u8; 32]) -> CoreResult<()> {
    let after_pgp = strip_encryption_suffix(wallet_id);
    let (clean_id, _) = strip_email_change_suffix(&after_pgp);
    verify_pk_binding_with_identity_key(&clean_id, &WALLET_IDENTITY_KEY, pk)
}

/// Verify pk binding with a specific identity key (for testing).
pub fn verify_pk_binding_with_identity_key(
    wallet_id: &str,
    master_pk: &[u8; 32],
    pk: &[u8; 32],
) -> CoreResult<()> {
    let (email, _checksum, pk_bind, salt) = parse_wallet_id(wallet_id)?;

    // Try all 7 (k, pt) combinations — pk_bind must match for at least one
    for &(k_byte, pt_byte, _name) in &WALLET_ID_PARAMS {
        let candidate = compute_pk_bind(&email, &salt, master_pk, pk, k_byte, pt_byte);
        if pk_bind == candidate.to_lowercase() {
            return Ok(());
        }
    }

    Err(ValidationError::InvalidWalletId)
}

/// Generate a wallet_id for an email with a given salt and pk (defaults to Standard: k=3, dmap)
///
/// Returns full wallet_id: "email/hex10" where hex10 = checksum(6) + pk_bind(2) + salt(2)
/// pk is bound via pk_bind for CL5 identity verification (prevents cheque theft).
///
/// SECURITY: Email is normalized before checksum computation.
pub fn generate_wallet_id(email: &str, salt: &str, pk: &[u8; 32]) -> CoreResult<String> {
    generate_wallet_id_with_identity_key(email, salt, &WALLET_IDENTITY_KEY, pk)
}

/// Generate a wallet_id with a specific identity key (defaults to Standard: k=3, dmap)
pub fn generate_wallet_id_with_identity_key(
    email: &str,
    salt: &str,
    master_pk: &[u8; 32],
    pk: &[u8; 32],
) -> CoreResult<String> {
    generate_wallet_id_full(email, salt, master_pk, pk, K_DEFAULT, PROOF_TYPE_DMAP)
}

/// Generate a wallet_id with explicit security level parameters.
/// Format: "email/checksum(6) + pk_bind(2) + salt(2)"
pub fn generate_wallet_id_full(
    email: &str,
    salt: &str,
    master_pk: &[u8; 32],
    pk: &[u8; 32],
    k_byte: u8,
    proof_type_byte: u8,
) -> CoreResult<String> {
    let normalized_email = normalize_email(email)?;

    // char-boundary-safe: `get` yields None on a short or mid-char index → "00".
    let salt_clean = salt.get(0..2).unwrap_or("00");

    let checksum = compute_checksum(&normalized_email, salt_clean, master_pk, k_byte, proof_type_byte);
    let pk_bind = compute_pk_bind(&normalized_email, salt_clean, master_pk, pk, k_byte, proof_type_byte);
    Ok(format!("{}/{}{}{}", normalized_email, checksum, pk_bind, salt_clean))
}

/// YPX-007: Generate all 7 wallet_ids for a wallet.
///
/// Returns Vec of (wallet_id, k, proof_type, display_name).
/// All 7 share the same email+salt but have different checksums and pk_binds.
pub fn generate_all_wallet_ids(email: &str, salt: &str, pk: &[u8; 32]) -> CoreResult<Vec<(String, u8, u8, &'static str)>> {
    generate_all_wallet_ids_with_identity_key(email, salt, &WALLET_IDENTITY_KEY, pk)
}

/// Generate all 7 wallet_ids with a specific identity key (for testing)
pub fn generate_all_wallet_ids_with_identity_key(
    email: &str,
    salt: &str,
    master_pk: &[u8; 32],
    pk: &[u8; 32],
) -> CoreResult<Vec<(String, u8, u8, &'static str)>> {
    let mut result = Vec::with_capacity(7);
    for &(k_byte, pt_byte, name) in &WALLET_ID_PARAMS {
        let wid = generate_wallet_id_full(email, salt, master_pk, pk, k_byte, pt_byte)?;
        result.push((wid, k_byte, pt_byte, name));
    }
    Ok(result)
}

