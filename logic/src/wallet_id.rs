//! wallet_id validation (YPX-007: Receiver-Defined Security Level)
//!
//! wallet_id format: "email/hex10" where hex10 = checksum(6) + pk_bind(2) + salt(2)
//! checksum = first_6_hex(BLAKE3(email || wallet_identity_key || salt || k_byte || proof_type_byte))
//! pk_bind = first_2_hex(BLAKE3("AXIOM_PK_BIND" || email || WALLET_IDENTITY_KEY || pk || salt || k || pt))
//!
//! Each wallet has 7 addresses (one per security level). Core extracts (k, proof_type)
//! from the checksum by trying all 7 combinations. The sender cannot choose or override
//! the security level — it is baked into the address itself.
//!
//! Example: "bob@example.com/a3f7b2ff32"
//!   - email: "bob@example.com"
//!   - checksum: "a3f7b2" (6 hex chars)
//!   - pk_bind: "ff" (2 hex chars, binds wallet to owner's Ed25519 pk)
//!   - salt: "32" (2 hex chars)
//!
//! This is for ANTI-TYPO protection + security level enforcement:
//!   - User types "bob@gmial.com/a3f7b232" (typo in gmail)
//!   - Checksum won't match → REJECT
//!   - Money NOT sent to wrong person!
//!
//! The wallet_identity_key is semi-public (extractable from core-logic)
//! but that's fine - security comes from signatures + consensus,
//! not from the checksum.
//!
//! SECURITY (C2): All email strings are normalized to:
//!   - Lowercase (ASCII only)
//!   - Trimmed whitespace
//!   - ASCII-only validation (rejects non-ASCII to prevent homoglyph attacks)

